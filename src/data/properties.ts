export const formatPrice = (price: number): string => {
  return new Intl.NumberFormat('en-US', {
    style: 'currency',
    currency: 'USD',
    minimumFractionDigits: 0,
    maximumFractionDigits: 0,
  }).format(price);
};

export const getPropertyById = (id: string): Property | undefined => {
  return properties.find(property => property.id === id);
};

export const getRelatedProperties = (currentId: string, limit: number = 3): Property[] => {
  const currentProperty = getPropertyById(currentId);
  if (!currentProperty) return [];
  
  return properties
    .filter(property => 
      property.id !== currentId && 
      (property.type === currentProperty.type || 
       property.address.city === currentProperty.address.city)
    )
    .slice(0, limit);
};

export const getFeaturedProperties = (limit: number = 4): Property[] => {
  return properties
    .filter(property => property.isFeatured)
    .slice(0, limit);
};

export const getRecentProperties = (limit: number = 4): Property[] => {
  return [...properties]
    .sort((a, b) => new Date(b.createdAt).getTime() - new Date(a.createdAt).getTime())
    .slice(0, limit);
};

export const filterProperties = (filters: SearchFilters): Property[] => {
  return properties.filter(property => {
    if (filters.location && 
        !property.address.city.toLowerCase().includes(filters.location.toLowerCase()) &&
        !property.address.state.toLowerCase().includes(filters.location.toLowerCase()) &&
        !property.address.neighborhood.toLowerCase().includes(filters.location.toLowerCase())) {
      return false;
    }
    
    if (filters.type && property.type !== filters.type) {
      return false;
    }
    
    if (filters.status && property.status !== filters.status) {
      return false;
    }
    
    if (filters.minPrice && property.price < filters.minPrice) {
      return false;
    }
    
    if (filters.maxPrice && property.price > filters.maxPrice) {
      return false;
    }
    
    if (filters.bedrooms && property.bedrooms < filters.bedrooms) {
      return false;
    }
    
    if (filters.bathrooms && property.bathrooms < filters.bathrooms) {
      return false;
    }
    
    if (filters.minSquareFeet && property.squareFeet < filters.minSquareFeet) {
      return false;
    }
    
    if (filters.maxSquareFeet && property.squareFeet > filters.maxSquareFeet) {
      return false;
    }
    
    if (filters.features && filters.features.length > 0) {
      const hasAllFeatures = filters.features.every(feature => 
        property.features.some(f => f.toLowerCase().includes(feature.toLowerCase()))
      );
      if (!hasAllFeatures) return false;
    }
    
    return true;
  });
};